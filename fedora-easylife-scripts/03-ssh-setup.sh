# Fun√ß√£o para testar conex√£o
test_connection() {
    local host="$1"
    local service="$2"
    
    print_info "Testando conex√£o com $service..."
    
    case "$service" in
        "GitHub")
            if ssh -T git@github.com 2>&1 | grep -q "successfully authenticated"; then
                print_success "Conex√£o com GitHub OK!"
            else
                print_error "Falha na conex√£o com GitHub"
            fi
            ;;
        "GitLab")
            if ssh -T git@gitlab.com 2>&1 | grep -q "Welcome to GitLab"; then
                print_success "Conex√£o com GitLab OK!"
            else
                print_error "Falha na conex√£o com GitLab"
            fi
            ;;
        *)
            if ssh -o ConnectTimeout=5 -o BatchMode=yes "$host" exit 2>/dev/null; then
                print_success "Conex√£o com $host OK!"
            else
                print_error "Falha na conex√£o com $host"
                print_info "Verifique se o servidor est√° rodando e se a chave foi adicionada ao authorized_keys"
            fi
            ;;
    esac
}

