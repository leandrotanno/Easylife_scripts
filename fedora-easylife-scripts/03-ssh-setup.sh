# Fun√ß√£o para configurar SSH agent
setup_ssh_agent() {
    print_info "Configurando SSH agent..."
    
    # Verificar se ssh-agent est√° rodando
    if ! pgrep -x "ssh-agent" > /dev/null; then
        eval "$(ssh-agent -s)"
    fi
    
    # Adicionar configura√ß√£o ao .bashrc/.zshrc
    local shell_rc=""
    if [[ "$SHELL" == *"zsh"* ]]; then
        shell_rc="$HOME/.zshrc"
    else
        shell_rc="$HOME/.bashrc"
    fi
    
    if ! grep -q "ssh-agent" "$shell_rc" 2>/dev/null; then
        echo '
# SSH Agent auto-start
if ! pgrep -x "ssh-agent" > /dev/null; then
    eval "$(ssh-agent -s)" > /dev/null
fi' >> "$shell_rc"
        print_success "SSH agent configurado no $shell_rc"
    fi
}

# Fun√ß√£o para gerar chave SSH
generate_ssh_key() {
    local service="$1"
    local email="$2"
    local key_name="$3"
    
    print_info "Gerando chave SSH para $service..."
    
    # Verificar se j√° existe
    if [[ -f "$HOME/.ssh/$key_name" ]]; then
        read -p "Chave $key_name j√° existe. Sobrescrever? [y/N]: " overwrite
        if [[ ! "$overwrite" =~ ^[Yy]$ ]]; then
            print_warning "Pulando gera√ß√£o da chave $key_name"
            return
        fi
    fi
    
    # Gerar chave
    ssh-keygen -t ed25519 -C "$email" -f "$HOME/.ssh/$key_name" -N ""
    
    # Adicionar ao ssh-agent
    ssh-add "$HOME/.ssh/$key_name"
    
    print_success "Chave $key_name gerada com sucesso!"
}

# Fun√ß√£o para configurar arquivo SSH config
setup_ssh_config() {
    local config_file="$HOME/.ssh/config"
    
    print_info "Configurando arquivo SSH config..."
    
    # Criar diret√≥rio .ssh se n√£o existir
    mkdir -p "$HOME/.ssh"
    chmod 700 "$HOME/.ssh"
    
    # Backup do config existente
    if [[ -f "$config_file" ]]; then
        cp "$config_file" "$config_file.backup.$(date +%Y%m%d_%H%M%S)"
        print_info "Backup do config criado"
    fi
    
    # Criar/atualizar config
    cat > "$config_file" << 'EOF'
# SSH Config - Auto-generated by ssh-setup.sh

# GitHub
Host github.com
    HostName github.com
    User git
    IdentityFile ~/.ssh/id_github
    IdentitiesOnly yes

# GitLab
Host gitlab.com
    HostName gitlab.com
    User git
    IdentityFile ~/.ssh/id_gitlab
    IdentitiesOnly yes

# VPS Principal
Host vps
Host meu-vps
    HostName SEU_VPS_IP_AQUI
    User root
    Port 22
    IdentityFile ~/.ssh/id_vps
    IdentitiesOnly yes

# VPS Secund√°rio (exemplo)
Host vps2
    HostName SEU_VPS2_IP_AQUI
    User ubuntu
    Port 22
    IdentityFile ~/.ssh/id_vps2
    IdentitiesOnly yes

# Servidor de desenvolvimento
Host dev-server
    HostName SEU_DEV_SERVER_IP_AQUI
    User developer
    Port 22
    IdentityFile ~/.ssh/id_dev_server
    IdentitiesOnly yes

# Configura√ß√µes globais
Host *
    AddKeysToAgent yes
    UseKeychain yes
    ServerAliveInterval 60
    ServerAliveCountMax 3
    IdentitiesOnly yes
EOF

    chmod 600 "$config_file"
    print_success "Arquivo SSH config configurado"
    print_warning "ATEN√á√ÉO: Edite $config_file e substitua os IPs pelos seus servidores reais!"
}

# Fun√ß√£o para mostrar chave p√∫blica
show_public_key() {
